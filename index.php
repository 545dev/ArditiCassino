         <h1>La nostra<strong> Storia</strong></h1>
         <p>
            Correva l'anno 1992, quando il primo gruppo di noi cominciò ad avvicinarsi al Softair, giocando vicino al lago.Ci alzavamo presto, la domenica mattina, per andare a Bracciano, in un campo dato in gestione ad uno dei primi gruppi formatosi in Italia, i Green Hunter , associazione presieduta, allora, da un giovane e sconosciuto Marcello Falcinelli.
         </p>
         <p>
            Percorrevamo 360 km per giocare un paio di ore. I primi tempi non ci pesava perché lentusiasmo era troppo ma, a lungo andare, le distanze i costi cominciavano a farsi sentire. Fu così che decidemmo di compiere il grande passo e di costituirci in associazione.
         </p>
         <p>
             Era il 28 arpile 1995. non avevamo però ancora un campo stabile dove praticare le nostre simulazioni. Per fortuna , il nostro primo presidente, Andrea Lancia conosceva Giancarlo Lucci, una guardia ambientale che riuscì a farci assegnare un magnifico fondo attraversato da un fiume.
         </p>
         <div class="toggle-wrap">
            <span class="toggle-title"><a href="#">Continua a Leggere</a></span>
            <div class="toggle_container">
               <p>
                   Il secondo passo fu iscriversi alla neonata A.S.N.W.G. già in quellanno era il 1996, disputammo il torneo nazionale arrivando secondi dietro le Aquile di La Spezia, senza mai aver perso un solo incontro. In due lustri abbiamo acquisito tanta esperienza, riuscendo ad organizzare tornei di un realismo più che soddisfacente.
               </p>
                Agli inizi del 2000 il club contava una ventina di associati ma con larrivo di forze fresche e in particolare di free bird, grande trascinatore ed organizzatore, le domande di iscrizione sono sensibilmente lievitate; con lingresso di GUNSMITH poi hanno fatto la loro comparsa le prime ASG custom, con la conseguenza che tutti i soci anziani hanno incominciato a rinnovare i propri arsenali. Nel frattempo siamo anche entrati nel circuito del Comitato Regionale Lazio e abbiamo contribuito a fondare il C.I.L.U.C ( Comitati Interregionale, Lazio Umbria e Campania).
               <p>
                  Siamo stati uno dei primi club in Italia ad organizzare tornei di carattere Storico, avendo la fortuna o sarebbe meglio dire la sfortuna di vivere su uno dei campi di battaglia più cruento della seconda Guerra Mondiale : MONTECASSINO. A tal riguardo abbiamo posto una particolare attenzione alla ricostruzione delle divise tedesche (attualmente ne possediamo 40). Addirittura allinterno dellassociazione è nato un gruppo che si occupa di rievocazioni storiche interpretando la PRIMA Divisione FALLSCHIRMJAGER (Paracadutisti Tedeschi) con armi originali (Kar98, mp40, Thomson, M1, Panzer Faust) e due veicoli funzionanti trasformati in jeep. Tutto questo è frutto dei sacrifici e del lavoro del nostro team. In questi anni abbiamo girato in tutti i campi dItalia, dalla Sicilia al Friuli, arricchendo il nostro palmares con numerosi trofei.
               </p>
                Per spegnere le fatidiche 10 candeline dalla fondazione il Gruppo Arditi Cassino ha organizzato un Four Flags indicando 3 club di altrettanti comitati: Legio Brutia (Calabria) Indians Termoli (Molise) e una rappresentativa della Campania. Dopo 3 ore di games senza fiato,sono stati premiati tutti i partecipanti.
               <p>
                  Passata l'estate, durante la quale abbiamo continuato ad allenarci, il 2 ottobre abbiamo piazzato 2 formazioni al 2° e al 4° posto del torneo di Termoli. La stagione in corso è la seconda da quando ci siamo affiliati al CSEN. Con il sostegno del responsabile provinciale di Frosinone, il Sig. Reali stiamo cercando di creare i presupposti per continuare a giocare almeno per un altro decade.
               </p>
